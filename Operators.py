#Calculate the profit earned by a sales person who has sold a refrigerator in Rs 25000 and cost price of refrigerator was Rs 19000. Calculate the profit percentage earned in this CP
import math
SP = 25000
print("Selling Price of Refrigerator",SP)
CP = 19000
print("Cost Price of Refrigerator",CP)
Profit = SP-CP
print("Profit Earned = ",Profit)
ProfitPercentage = math.ceil((Profit/CP)*100)
print("Profit Percentage Earned on the Cost Price of Refrigerator = ",ProfitPercentage,"%")


